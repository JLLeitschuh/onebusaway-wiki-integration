package org.onebusaway.wiki.xwiki.impl;

public class XWikiPageLink {
  
  private String rel;
  private String href;
  
  public String getRel() {
    return this.rel;
  }
  
  public void setRel(String rel) {
    this.rel = rel;
  }
  
  public String getHref() {
    return this.href;
  }
  
  public void setHref(String href) {
    this.href = href;
  }
}
